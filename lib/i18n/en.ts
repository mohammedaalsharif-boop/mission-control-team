// Widen literal string types so Arabic (and other locales) can use different string values
type DeepWiden<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
    ? readonly string[]
    : T[K] extends object
    ? DeepWiden<T[K]>
    : T[K];
};

export type Translations = DeepWiden<typeof en>;
